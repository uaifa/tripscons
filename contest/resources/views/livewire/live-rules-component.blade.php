Liability
</h2>
<p>
The contest organizers will not be responsible for any loss or damage to the submitted photos. Participants are responsible for ensuring that they have the right to use any identifiable persons, locations, or objects in their photos.
</p>
<h2 class="main-title">
Public voting
</h2>
<p>
The contest organizers reserve the right to disqualify any votes that are suspected to be fraudulent or in violation of the contest rules.
</p>
<h2 class="main-title">
Announcement of winners
</h2>
<p>
The winners can see the contest results on the Contest Page on tripscon.com and will be officially notified by email, phone and social media on <strong>21 April 2023</strong>. The winner’s name and photo will be published on the contest website and social media channels. The winner will receive his/her AWARD at Tripscon’s Head office
</p>
