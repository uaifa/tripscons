        <h2 class="main-title-large">
        Contest Rules
        </h2>
        <h2 class="main-title">
        Eligibility
        </h2>
        <p>
        The contest is open to all general public, whether amateurs or professionals without any gender specification except for the contest organizers and their immediate family members. Any photo captured through Mobile cam, Drone or DSLR cam is eligible for the contest. Age limit for participation in the contest is 18 and above.
        </p>
        <h2 class="main-title">
        Contest dates
        </h2>
        <p>
        Voting will continue from 22 March 2023 till 20 April 2023 (PKT 11:59 pm).
        <br>
        The deadline for submissions is 20 April 2023. Entries received after this date will not be considered.
        </p>

        <h2 class="main-title">
        Theme
        </h2>

<p>
The theme of the contest is specific to Pakistan, and all entries must adhere to the following theme.
<br>
Nature & Landscapes: Landscapes, outdoor scenes, nature photos, sunsets, sunrise, urban landscapes, seascapes, cityscapes, farms, floral, etc.

        </p>

<h2 class="main-title">
Submission guidelines
</h2>
<p>
Each participant is allowed to submit a maximum of 1 photo entry. Photos should be high resolution jpg and must not exceed 10MB in size.
Photos with a date stamp or photographer’s name on the photo will be disqualified.
All submissions must be the original work of the entrant, photos entered in previous contests may not be resubmitted. All photos must be original work, taken by the entrants. No third party may own or control any materials the photo contains, and the photo must not infringe upon the watermark, trademark, copyright, moral rights, intellectual rights, or rights of privacy of any entity or person.

</p>
<h2 class="main-title">
Photo captions
</h2>
<p>
Each photo must be accompanied by a caption that includes the title of the photo and a location.
</p>
<h2 class="main-title">
Photo editing
</h2>
<p>
Photos may be slightly edited for very basic adjustments such as brightness, contrast, color balance and cropping. However, excessive manipulation or alteration of the original photo is not allowed. Image editing filters are strictly not allowed.
</p>

<h2 class="main-title">
Ownership and copyright
</h2>
<p>
By submitting photos to the contest, participants confirm that the photos are their original work and do not infringe on any copyright laws. Participants also grant the contest organizers a non-exclusive, irrevocable, royalty-free license to use the photos for promotional purposes.
</p>
<h2 class="main-title">
Winner’s criteria
</h2>
<p>
This contest is based on the number of votes and the voting process is simple. Eligible Participants will upload their photo entries and can vote to their own photo once in 24 hours. They can share their photo link to their Facebook/Instagram feed, stories, and reels to get maximum votes. Once any of your fans votes on your photo, will have the option to vote again after 24 hours. The photo with maximum votes will be considered as winner of the contest.
</p>
<h2 class="main-title">
Prize
</h2>
<p>
The winner will receive a best photo Award in the mentioned category along with a <strong>FREE</strong> 5 Days Hunza group trip in the best available dates. This <strong>FREE TRIP</strong> will be offered from Lahore to Hunza to Lahore. This <strong>FREE TRIP</strong> is not transferable, and no cash or other alternatives will be offered.
</p>
<h2 class="main-title">
Disqualification
</h2>
<p>
Entries that are incomplete, illegible, immoral or do not comply with the contest rules will be disqualified. Violators will be removed from the contest, stripped of any prize(s), and banned from entering future contests.
Entries will not be accepted unless submitted via the official contest channel. Entries not submitted through the proper channel will be deleted.

</p>
